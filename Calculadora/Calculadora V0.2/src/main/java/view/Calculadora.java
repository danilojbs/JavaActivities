    private void setValor(String num){
        if(operador == null){
            if(valor1 == null){
                valor1 = num;
            }else {
                valor1 += num;
            }
        } else {
            if(valor2 == null){
                valor2 = num;
            } else {
                valor2 += num;
            }
        }
    }
    
    private void setValorNegativo(String num){
        if(operador == null){
            if(valor1 == null){
                valor1 = num;
                negativo = true;
                Text_resultado.setText(valor1);
            }else {
                if (negativo == true){
                    valor1 += num;
                } else if(negativo == false){
                    String x = valor1;
                    valor1 = num;
                    valor1 += x;
                    negativo = true;
                    Text_resultado.setText(valor1);
                }
            }
            }
         else {
            if(valor2 == null){
                valor2 = num;
                negativo = true;
                Text_resultado.setText(valor1 + " " + operador + " " + valor2);
            }else {
                if (negativo == true){
                    valor2 += num;
                } else if(negativo == false){
                    String x = valor2;
                    valor2 = num;
                    valor2 += x;
                    negativo = true;
                    Text_resultado.setText(valor1 + " " + operador + " " + valor2);
                }
        }
        }
    }
    
    private void setValorQuadrado(){
        if(operador == "^"){
            if(valor1 != null){
                Float x = Float.valueOf(valor1);
                Float z = x * x;
                valor1 = String.valueOf(z);
            }
        } else {
            if(valor2 != null){
                Float x = Float.valueOf(valor2);
                Float z = x * x;
                valor2 = String.valueOf(z);
            }
        }
    }
    
    private void checarVazio(){
        if(valor1 == null && valor2 == null && operador == null && negativo == false && !Text_resultado.getText().isEmpty()){
            Text_resultado.setText(null);
        }
    }
    
    private void checar(){
        if(valor1 == null && valor2 == null && operador == null && negativo == false && !Text_resultado.getText().isEmpty()){
            setValor(Text_resultado.getText());
        }
    }
    
