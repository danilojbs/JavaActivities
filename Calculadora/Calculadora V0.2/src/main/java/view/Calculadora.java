    private void Bot_igualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Bot_igualActionPerformed
        Float avalor1 = Float.valueOf(valor1);
        Float avalor2 = Float.valueOf(valor2);
        if(operador == "+"){
            Text_resultado.setText(String.valueOf(avalor1 + avalor2));
        }else{
            if(operador == "-"){
                Text_resultado.setText(String.valueOf(avalor1 - avalor2));
            }
            else {
                if(operador == "x"){
                    Text_resultado.setText(String.valueOf(avalor1 * avalor2));
                }
                else {
                    if(operador == "/"){
                        Text_resultado.setText(String.valueOf(avalor1 / avalor2));
                    }
                    else {
                        if(operador == "%"){
                            Text_resultado.setText(String.valueOf((avalor1 / 100) * avalor2));
                        }
                    }
                }
            }
        }
        valor1 = null;
        valor2 = null;
        operador = null;
        virgula = false;
        negativo = false;
    }//GEN-LAST:event_Bot_igualActionPerformed

